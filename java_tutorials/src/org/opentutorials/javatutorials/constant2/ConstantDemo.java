package org.opentutorials.javatutorials.constant2;

public class ConstantDemo {
    public static void main(String[] args) {
        /*
         * 1. 사과
         * 2. 복숭아
         * 3. 바나나
         */
        int type = 1;
        switch(type){
            case 1:
                System.out.println(60);
                break;
            case 2:
                System.out.println(50);
                break;
            case 3:
                System.out.println(100);
                break;
        }
    }
 
}