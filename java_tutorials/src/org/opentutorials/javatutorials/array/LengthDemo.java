package org.opentutorials.javatutorials.array;

public class LengthDemo {
		public static void main(String[] args) {
			String[] classGroup = new String[4];
			classGroup[0] = "A";
			System.out.println(classGroup.length);			
			classGroup[1] = "B";
			System.out.println(classGroup.length);
			classGroup[2] = "C";
			System.out.println(classGroup.length);
			classGroup[3] = "D";
			System.out.println(classGroup.length);
		}
}
